DROP TABLE IF EXISTS User;

CREATE TABLE User (
    id INT AUTO_INCREMENT PRIMARY KEY, 
    name VARCHAR(55) NOT NULL, 
    num_card VARCHAR(16) NOT NULL UNIQUE,
    pin VARCHAR(4) NOT NULL, 
    money DOUBLE NOT NULL
);

